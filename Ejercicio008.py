x, y = 12, 10

if(x == y):
    print("1. x es igual a y")
else:
    print("1. x no es igual a y")

if(x != y):
    print("2. x no es igual a y")
else:
    print("2. x es igual a y")

if(x > y):
    print("3. x es mayor a y")
else:
    print("3. x no es mayor a y")

if(x < y):
    print("4. x no es mayor a y")
else:
    print("4. x es mayor a y")

if(x <= y):
    print("5. x es menor o igual a y")
else:
    print("5. x no es menor o igual a y")

if(x >= y):
    print("6. x es mayor o igual a y")
else:
    print("6. x no es mayor o igual a y")
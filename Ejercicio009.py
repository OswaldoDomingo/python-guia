z *= y
print("3. valor de z cuando realizas la siguiente operación -·- z *= x", z)

z /= y
print("4. valor de z cuando realizas la siguiente operación -·- z/= x", z)

z = 2
z %= x
print("5. Asignamosa a z = 2 y hayamos el módulo  valor de z %= x y el valor de z es ", z)


z //= x

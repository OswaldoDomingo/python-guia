# Membership operators
# Ejercicio11.
# Operador Significado
# in Devuelve True si la variable o variable se encuentra en la secuencia.
# not in Devuelve True si la variable o valor no se encuentra en la secuencia

x = "Hola mundo"
y = {1:"x", 2:"y"}

print("H" in x)

print("Hola" not in x)

print(2 in y)

print("z" in y)